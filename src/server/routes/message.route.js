import express from 'express';
import * as messageCtrl from '../controllers/message.controller';
import isAuthenticated from '../middlewares/authenticate';
import validate from '../config/joi.validate';
import schema from '../utils/validator';

const router = express.Router();

/**
 * @swagger
 * tags:
 *   - name: message
 *     description: Message operations
 */

/**
 * @swagger
 * definitions:
 *   Message:
 *     type: object
 *     properties:
 *       id:
 *         type: integer
 *         description: Unique identifier representing a specific message
 *         example: 2
 *       first_name:
 *         type: string
 *         description: first name of the message
 *         example: Krishna
 *       last_name:
 *         type: string
 *         description: last name of the message
 *         example: Timilsina
 *       email:
 *         type: string
 *         description: email of the message
 *         required: true
 *         example: test@gmail.com
 *       password:
 *         type: string
 *         description: password of the message
 *         required: true
 *         example: "1234"
 *       status:
 *         type: integer
 *         description: status of the message
 *         example: 1
 *       created_at:
 *         type: string
 *         format: date-time
 *         description: Message creation datetime
 *       updated_at:
 *         type: string
 *         format: date-time
 *         description: Message update datetime
 *   Error:
 *     type: object
 *     properties:
 *        message:
 *           type: string
 *        error:
 *           type: boolean
 *           default: true
 */

/**
 * @swagger
 * securityDefinitions:
 *   Bearer:
 *     description: |
 *            For accessing the API a valid JWT token must be passed in all the queries in
 *            the 'Authorization' header.
 *
 *
 *            A valid JWT token is generated by the API and returned as answer of a call
 *            to the route /auth/login giving a valid message & password.
 *
 *
 *            The following syntax must be used in the 'Authorization' header:
 *
 *            Bearer xxxxxx.yyyyyyy.zzzzzz
 *     type: apiKey
 *     name: Authorization
 *     in: header
 *
 */

router.route('/')

/**
 * @swagger
 * /messages:
 *   post:
 *     tags:
 *       - message
 *     summary: "Create a new message"
 *     security:
 *        - Bearer: []
 *     operationId: storeMessage
 *     consumes:
 *       - application/json
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: body
 *         in: body
 *         description: Created message object
 *         required: true
 *         schema:
 *           $ref: "#/definitions/Message"
 *     responses:
 *       200:
 *         description: OK
 *         schema:
 *           $ref: "#/definitions/Message"
 *       403:
 *          description: Message not found
 *          schema:
 *             $ref: '#/definitions/Error'
 */

    .post(validate(schema.storeMessage), (req, res) => {
        messageCtrl.store(req, res);
    })

    /**
     * @swagger
     * /messages:
     *   get:
     *     tags:
     *       - message
     *     summary: "List all messages"
     *     operationId: findAll
     *     consumes:
     *       - application/json
     *     produces:
     *       - application/json
     *     parameters: []
     *     responses:
     *       200:
     *         description: OK
     *         schema:
     *            type: object
     */

    .get( (req, res) => {
        messageCtrl.findAll(req, res);
    });


router.route('/:id')

/**
 * @swagger
 * /messages/{id}:
 *   get:
 *     tags:
 *       - message
 *     summary: Find the message by ID
 *     operationId: findById
 *     consumes:
 *       - application/json
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: id
 *         in: path
 *         description: id of message that needs to be fetched
 *         required: true
 *         type: integer
 *     responses:
 *       200:
 *         description: OK
 *         schema:
 *           $ref: "#/definitions/Message"
 *       404:
 *          description: Message not found
 *          schema:
 *             $ref: '#/definitions/Error'
 */

    .get( (req, res) => {
        messageCtrl.findById(req, res);
    })

    /**
     * @swagger
     * /messages/{id}:
     *   put:
     *     tags:
     *       - message
     *     summary: "Update an existing message by ID"
     *     security:
     *       - Bearer: []
     *     operationId: update
     *     consumes:
     *       - application/json
     *     produces:
     *       - application/json
     *     parameters:
     *       - name: id
     *         in: path
     *         description: id that need to be updated
     *         required: true
     *         type: integer
     *       - name: body
     *         in: body
     *         description: Updated message object
     *         required: true
     *         schema:
     *           $ref: "#/definitions/Message"
     *     responses:
     *       200:
     *         description: OK
     *         schema:
     *           $ref: "#/definitions/Message"
     *       400:
     *         description: Invalid message
     */

    .put(isAuthenticated, (req, res) => {
        messageCtrl.update(req, res);
    })

    /**
     * @swagger
     * /messages/{id}:
     *   delete:
     *     tags:
     *       - message
     *     summary: Delete the message by ID
     *     security:
     *       - Bearer: []
     *     operationId: destroy
     *     produces:
     *       - application/json
     *     parameters:
     *       - name: id
     *         in: path
     *         description: id of message that needs to be deleted
     *         required: true
     *         type: integer
     *     responses:
     *       200:
     *         description: OK
     *       400:
     *          description: "Invalid ID"
     */

    .delete(isAuthenticated, (req, res) => {
        messageCtrl.destroy(req, res);
    });


export default router;